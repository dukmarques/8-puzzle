     private void updateBoard() {
        Integer[][] blocks = this.controller.getBlocksOfBoard();
        this.button_1.setText(this.checkIfZero(blocks[0][0]));
        this.button_2.setText(this.checkIfZero(blocks[0][1]));
        this.button_3.setText(this.checkIfZero(blocks[0][2]));
        this.button_4.setText(this.checkIfZero(blocks[1][0]));
        this.button_5.setText(this.checkIfZero(blocks[1][1]));
        this.button_6.setText(this.checkIfZero(blocks[1][2]));
        this.button_7.setText(this.checkIfZero(blocks[2][0]));
        this.button_8.setText(this.checkIfZero(blocks[2][1]));
        this.button_9.setText(this.checkIfZero(blocks[2][2]));
        
        this.movementsLabel.setText("Total de Movimentos: " + this.movementCounter);
    }
    
    private String checkIfZero(Integer value) {
        return value != 0 ? Integer.toString(value) : "";
    }
    
